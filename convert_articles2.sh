A MAN11 CAPITAL iniciou seu portfolio em março de 2018 com R$31.020,00.

Um pouco antes disso, quando idealizei a criação de um portfolio, decidi olhar pra ele não como uma carteira de ações comum e sim como o início de um império. Parece algo bobo, eu sei, mas muitos dos grandes investidores fizeram a mesma coisa. Pouco dinheiro, muita confiança na performance e uma visão diferente de quem se encontra nessa situação.

Buffett, Marks, Dalio, Thorp e muitos outros começaram pequeno, mas olhando para aquela pequena porção como se fosse apenas o início de algo muito grande e, por isso, deveria ser tratado como tal.

Esse tipo de abordagem te faz ser mais responsável com aquilo. Você começa a tratar seu portfolio e sua performance como os Afrescos da Capela Sistina de Michelangelo.

Voltando ao prumo, é bem claro que dado o trabalho de construir sua obra-prima, você terá cuidado com cada pincelada, ou seja, cada decisão de compra ou venda. Você não quer uma porr@ de uma mancha na cara de sua Mona Lisa! Erros acontecem e podem ser corrigidos durante a pintura, mas um grande erro pode arruiná-la.

Quando Buffett decidiu em 1983 que de fato a Berkshire Hathaway seria sua obra-prima, o valor investido por ele por ação foi de apenas U$32,45, e hoje, após várias pinceladas, cada ação vale U$380.000,00.

*Chupa Michelangelo!*

Aproveitando essa belíssima sexta-feira, venho te fazer uma proposta!

O que acha de zerar tudo e começar a construir seu império agora?

Separe uma conta específica ou duas, e destine-as apenas para a construção de seu império, a sua Berkshire Hathaway.

Veja bem, pois cada decisão sua influenciará na tela final, e portanto é bom se concentrar em fazer o que sabe fazer, e claro, não dá pra ficar tirando pedaços dela por qualquer motivo. Sendo assim, ela só deve crescer, com cuidado, mas só crescer.

Interessante dizer aqui que Buffett levava tão a sério essa parte de não arrancar pedaços de sua obra, que se viu enforcado financeiramente em certo momento, devido ao seu baixo salário na Berkshire e um repentino aumento em suas despesas por motivos pessoais. Essa é uma das partes da vida dele que os holders não contam, quando em uma conta pessoal, ele fez mais de 3 milhões de dólares num curto espaço de tempo, ESPECULANDO em diversos mercados. Deixarei esse causo pra outra carta.

