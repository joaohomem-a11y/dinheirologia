Sempre que ouvimos alguém no Mercado dizer que algo "estava tão claro", ou qualquer coisa parecida, poderá observar que o verbo sempre estará conjugado no passado.

Isso porque nada no Mercado é tão claro assim no presente.

Nossas decisões tanto nos trades quanto nas decisões para um portfólio de investimentos são tomadas à meia-luz.

Se nos momentos decisivos do Mercado tudo fosse tão claro, não haveria ninguém do outro lado do book.

Apenas analistas e "profetas que profetizam o passado" atrevem-se a dizer que algo no Mercado estava muito claro.

Já que gostaram do exemplo de semana passada, vou repetir nesta carta.

Ao final dos anos 60, os preços vinham batendo máximas sobre máximas, com a euforia, o otimismo e o apetite por risco em níveis *exuberantemente irracionais*.

Neste cenário típico de final de ciclo de alta, o sell side do Mercado batia no peito e bradava coisas como "é o fim do value investing", "a segurança de se investir em Bolsa", "como ficar milionário rápido", "os mais experientes estão obsoletos", "é tempo de uma nova metodologia", "as novas tecnologias mudarão a dinâmica do Mercado", "Growth! Growth! Growth!"...

Alguma semelhança com os dias de hoje?

Neste mesmo momento, um investidor experiente, com resultados excepcionais, morador de Omaha e visto como obsoleto por alguns, citou em uma de suas cartas aos sócios de 1966:

> Permitam-me sugerir mais uma vez que o futuro nunca foi claro para mim (liguem para nós quando os próximos meses forem óbvios para vocês - ou pelo menos, as próximas horas).

Bom, se até Buffett toma suas decisões sem ter total clareza sobre o futuro, por que cargas d'água você acha que vai clicar apenas quando estiver tudo muito claro?

- Será que essa barra forte é uma barra de start, uma barra de esgotamento ou uma barra climática?
- Será que Petrobras cai mais ou já bateu no fundo?
- É melhor esperar tudo ficar mais claro antes de tomar minha decisão...

No Mercado financeiro, costumo usar uma expressão que remete a tudo isso:

Piscou, perdeu!

Se deseja ser um Fazedor, precisa tomar decisões quando tudo está nebuloso ainda e pior, enquanto se escuta gritos ou chamados.

Sim, é bem difícil. Mas ninguém te prometeu que seria fácil!

> Investir com sucesso é fazer com que as pessoas concordem com você... mais tarde. - James Grant

Até a próxima semana!

Devemos agir à meia-luz!

*João Homem*
