Ok, lá vem o João com um título de conteúdo completamente sem sentido. E pior, começando o texto com um ok, sendo a primeira frase algo que talvez estaria em minha mente, e não na dele.

Sim, a frase acima veio de sua cabeça! Porém, quanto a fazer sentido, saiba que o sentido das e nas coisas depende mais de nossa percepção do que de nossa interpretação de textos. Aliás, a interpretação depende da percepção.

Creio que todos que venham a consumir este espartano conteúdo são pessoas que gostam muito do Mercado e querem muito evoluir profissionalmente nisso. Sendo assim, eu pergunto:

Por que não ligaram o título da minha série de cartas ao talvez maior investidor de todos os tempos?

Sim, o título é quase uma homenagem ao nosso querido velhinho Warren Buffett.

Esse é o título que seu avô Ernest Buffett deu a um protótipo de um livro de sua autoria, o qual fazia questão de ditar algumas páginas todas as noites para o jovem Warren, quando seu neto veio morar com ele.

Você saberia disso se tivesse lido o relatório anual da Berkshire Hathaway de 1985.

Entendeu a questão da percepção?

> Quando eu tinha 12 anos, morei com meu avô por aproximadamente quatro meses. Um merceeiro de profissão, ele também estava trabalhando em um livro e todas as noites ele ditava algumas páginas para mim. O título era "How to Run a Grocery Store and a Few Things I Have Learned About Fishing". Meu avô tinha certeza que o interesse por estes dois assuntos era universal e que o mundo aguardava suas opiniões. Você pode concluir a partir do título e dos conteúdos desta seção que fui superexposto ao estilo literário (e à personalidade) do vovô. - Warren Buffett

O título da seção mencionada por Buffett era "Three Very Good Businesses (and a Few Thoughts About Incentive Compensation)".

Sim, aquele guruzão que te ensinará a ser o próximo Buffett nunca mencionou isso. Ele precisa fazer o trabalho dele, de ficar na superficialidade sexy das histórias, o que certamente é mais comercial.

Compre ações de boas empresas, mantenha-as e em breve será um bilionário gente boa!

Por isso a pergunta do título.

Até onde você quer ir em seu entendimento?

Acha que realmente o mercado é apenas uma questão matemática?

Pior, acha que basta seguir as indicações que seguirá rumo aos bilhões?

Vejo muita gente falando de mim como se eu fosse um cara fora da curva na questão de entender o mercado, como se fosse um mago ou até mesmo um gênio. Porém, a única coisa que vejo de diferente em mim é que sempre quero ir mais fundo. Na época da escola, muitos professores me odiavam por isso.

Ah, tenho outra característica...

Eu me divirto muito lendo conteúdos viscerais de Mercado! Aprender se divertindo é algo mágico mesmo! Está aí a magia mencionada no parágrafo acima.

É exatamente isso que posso te prometer nessa minha série nas *Cartas de Um Capitalista*, algo visceral, de alguém que faz na real, ama o que faz e estará cada vez indo mais fundo na Toca do Coelho.

Quer vir junto?

Na próxima sexta cavaremos mais e, claro, nos divertiremos!

