Um magnata do petróleo chega à porta do céu e ouve a perturbadora notícia:

— O Conjunto Residencial destinado aos homens que trabalham com petróleo está lotado!

Conseguindo que São Pedro lhe permitisse dizer algumas palavras, ele gritou:

— Descobriram petróleo no Inferno!

Ao que todos os petroleiros saíram correndo para as profundezas.

Impressionado, São Pedro disse-lhe:

— Agora há bastante lugar.

O magnata então fez uma pausa...

— É, acho que vou junto com o resto dos rapazes. Afinal, pode ser que haja alguma verdade nesse boato.

A história acima era contada por Benjamin Graham a seus alunos e colegas, para descrever os momentos de euforia dos mercados, onde uma narrativa atraente, composta apenas de palavras, pode levar até mesmo pessoas inteligentes a caírem em armadilhas.

Como exponho minha performance no portfólio MAN11, leio muitas mensagens perguntando o motivo de eu não ter esse ou aquele ativo no portfolio. 100% dessas mensagens fazem referência aos ativos da moda.

Em minha humilde opinião, se está na moda, ou seja, se até seu vizinho te pergunta sobre aquele ativo num churrasco, este não é mais uma oportunidade.

Quanto mais eufóricos os mercados, mais as pessoas diminuem os riscos em seus cenários. Vemos, ainda mais hoje em dia com redes sociais, pessoas se gabando com lucros extraordinários e então vem aquele sentimento de estar ficando de fora de uma festa incrível (FOMO).

O que a grande maioria não pensa é que lucro nominal é diferente de lucro realizado. Depois que aquela pessoa vê seus lucros nominais derretendo, ela não volta para as redes sociais para dizer o que aconteceu com aquela sua operação especulativa.

Isso também acontece no daytrade.

Vimos essa semana a subida mais rápida da história do nosso Índice Futuro. Quem estava posicionado na compra ou com uma ordem de compra um pouco acima do preço nesse momento, lucrou. Muitos desses foram para as redes sociais matar as vontades de seus próprios egos, e claro, se posicionarem como os gênios do Mercado. "Era só..."

Amanhã eles devolvem tudo e não vão nas redes sociais dizer os motivos de terem errado.

Este é o circo em volta do Mercado! Tudo é previsível, tudo é capaz de gerar lucros com a técnica correta.

Controle-se!

Euforia e pânico não podem fazer parte de nossas decisões.

O Mercado não é um Conjunto Residencial no Céu com uma multidão, mas você também não precisa sair correndo para o Inferno só porque todo mundo está indo pra lá.

Até a próxima sexta e que se foda o petróleo no Inferno!

